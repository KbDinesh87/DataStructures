/* WORKER TABLE */
CREATE TABLE Worker (
	WORKER_ID int NOT NULL, 
    FIRST_NAME varchar(255),
	LAST_NAME VARCHAR(255),
	SALARY INT,
	JOINING_DATE DATETIME,
	DEPARTMENT VARCHAR(255),
    PRIMARY KEY(WORKER_ID)
);
